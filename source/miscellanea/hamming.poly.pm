◊h1{Hamming: You and Your Research}

◊section{

 ◊p{Richard Hamming's talk on ◊em{You and Your Research} had a
    profound impact on my professional career. The video is available
    on ◊(link "https://www.youtube.com/watch?v=a1zDuOPkMSw"
    "Youtube"), and I've noted a few particularly relevant quotes with
    timestamps below.}

 ◊ul{
  ◊li{◊(link "https://www.youtube.com/watch?v=a1zDuOPkMSw&t=152s" "(02:32)")
      As far as I know, and as far as you know, you have one life to
      lead. You might as well lead a life you would like to have, and
      I suggest you a life of doing something signficant, by your
      definition of significant...}

  ◊li{◊(link "https://www.youtube.com/watch?v=a1zDuOPkMSw&t=892s" "(14:50)")
      If what you're doing is not important or not likely to be
      important, why are you doing it? ... It's that simple. If you
      don't work on important problems, you are not going to do
      important things except by the dumbest of dumb luck.}

  ◊li{◊(link "https://www.youtube.com/watch?v=a1zDuOPkMSw&t=1060s" "(17:40)")
       When you have a vision, you will go a long way. Without a
       vision of what you're going to do and where you're going to be,
       you're not going to get very far. It's that simple. You have to
       get a vision of what you are going to do and be, and then
       pursue it.}

  ◊li{◊(link "https://www.youtube.com/watch?v=a1zDuOPkMSw&t=1604s" "(26:44)")
      Let me reorganize my life. Let me quit spending my time reading
      nonsense magazines and thumbing through the newspaper—they're
      not very important to my career. Let's spend my time studying
      things for my career.}

  ◊li{◊(link "https://www.youtube.com/watch?v=a1zDuOPkMSw&t=152s" "(27:50)")
       It's not hard to do, you just do it.}}}

